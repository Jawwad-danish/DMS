    private void submit() {

        validateFormData();
        updateCalculations();

        if (!TableService.isTableEmpty(tableSaleItem)
                && !TableService.isTableEmpty(tableEmployeeName)
                && !fieldCustomerID.getText().equals("")
                && !fieldCustomerName.getText().equals("")
                && !fieldAreaName.getText().equals("")
                && !fieldSubAreaName.getText().equals("")) {

            double netValue;
            netValue = Double.valueOf(this.fieldNetTotal.getText());

            if (netValue >= 0) {
                new Form_SubmitWarning(this, "Sale").setVisible(true);
                this.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "Received Amount is greater thn NetAmount!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Incomplete Details");
        }
    }
