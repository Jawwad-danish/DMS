    private void tableSaleItemKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableSaleItemKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_SHIFT && tableSaleItem.getSelectedColumn() == COLUMN_ITEM_ID) {
            new Form_ProductSeletionView(this, tableSaleItem).setVisible(true);
            this.setEnabled(false);
        }
        if (evt.getKeyCode() == KeyEvent.VK_TAB) {

            //for R.Qty column
            if (tableSaleItem.getSelectedColumn() == COLUMN_REPLACE) {
                TABKeyPressRQtyColumn();
            }
            //for Name Column
            if (tableSaleItem.getSelectedColumn() == COLUMN_ITEM_NAME) {
                TABKeyPressNameColumn();
            }
            if (tableSaleItem.getSelectedColumn() == COLUMN_EXTRA_DISC) {
                TABKeyPressExtraDiscColumn();
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_R) {
            this.fieldReceived.setText("");
            this.fieldReceived.requestFocus();
        }
        if (evt.getKeyCode() == KeyEvent.VK_C) {
            this.fieldCustomerID.requestFocus();
        }
        if (evt.getKeyCode() == KeyEvent.VK_U) {
            tableSaleItem.changeSelection(0, 1, false, false);
            validateFormData();
            updateCalculations();
        }


    }//GEN-LAST:event_tableSaleItemKeyPressed

