    private void TABKeyPressNameColumn() {
        if (tableSaleItem.getValueAt(tableSaleItem.getSelectedRow(), COLUMN_ITEM_ID) != null) {
            try {
                saleModule_.fetchProductDetailsToSaleItemTable();
            } catch (Exception ex) {

            }
        }
    }

    private void TABKeyPressRQtyColumn() {
        tableSaleItem.changeSelection(tableSaleItem.getSelectedRow(), COLUMN_EXTRA_DISC - 1, false, false);
    }

    private void TABKeyPressExtraDiscColumn() {
        if (tableSaleItem.getValueAt(tableSaleItem.getSelectedRow(), COLUMN_ITEM_ID) != null) {

            if (tableSaleItem.getSelectedRow() == tableSaleItem.getRowCount() - 1) {
                tableModelSaleItemTable_.addRow(new Object[]{null, null, null, null, null, null, null, null});
            }
            tableSaleItem.changeSelection(tableSaleItem.getSelectedRow(), COLUMN_AVAILABLE, false, false);
        } else {
            tableSaleItem.changeSelection(tableSaleItem.getSelectedRow(), 0, false, false);
        }
    }

    private void validateFormData() {
        tableSaleItem.clearSelection();
        TableService.setValueToEmptyColumn(tableModelSaleItemTable_, COLUMN_SOLD_QTY, 0);
        TableService.setValueToEmptyColumn(tableModelSaleItemTable_, COLUMN_REPLACE, 0);
        TableService.setValueToEmptyColumn(tableModelSaleItemTable_, COLUMN_EXTRA_DISC, 0.0);
        TableService.setValueToEmptyColumn(tableModelSaleItemTable_, COLUMN_FIXED_DISC, 0.0);
        TableService.deleteRowWithEmptyColumn(tableModelSaleItemTable_, COLUMN_ITEM_ID);
        TableService.deleteRowWithColumnsValue(tableModelSaleItemTable_, new int[]{COLUMN_SOLD_QTY, COLUMN_REPLACE}, 0);
        if (fieldReceived.getText().equals("")) {
            fieldReceived.setText(String.valueOf(0.0));
        }
    }

