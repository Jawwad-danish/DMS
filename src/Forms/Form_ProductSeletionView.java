    private void fieldSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldSearchKeyReleased
        // TODO add your handling code here:
        if (!(evt.getKeyCode() == KeyEvent.VK_DOWN)) {
            TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(this.tableProductList.getModel());
            this.tableProductList.setRowSorter(rowSorter);
            String text = fieldSearch.getText();
            if (text.trim().length() == 0) {
                rowSorter.setRowFilter(null);
            } else {
                rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
            }
        }
    }//GEN-LAST:event_fieldSearchKeyReleased

    private void fieldSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldSearchKeyPressed
        // TODO add your handling code here:.
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            parentForm_.setEnabled(true);
            this.dispose();
        }
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            if (!TableService.isTableEmpty(tableProductList)) {
                tableProductList.requestFocus();
                tableProductList.changeSelection(0, 0, false, false);
            }
        }

    }//GEN-LAST:event_fieldSearchKeyPressed

