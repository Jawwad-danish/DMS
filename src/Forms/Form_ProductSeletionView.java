    private void tableProductListKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableProductListKeyPressed
        // TODO add your handling code here:
        Object productID;
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            productID = tableProductList.getValueAt(tableProductList.getSelectedRow(), 0);
            parentTable.setValueAt(productID, parentTable.getSelectedRow(), 0);
            parentForm_.fillSaleItemTableRowWithProduct();
            parentForm_.setEnabled(true);
            this.dispose();
        }
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            parentForm_.setEnabled(true);
            this.dispose();
        }

    }//GEN-LAST:event_tableProductListKeyPressed

