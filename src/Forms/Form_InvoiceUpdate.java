    private void submit(){
        try{
        double amount = Double.valueOf(fieldAmount.getText());
        double balance = parentForm_.getSelectedBalance();

        if (!fieldAmount.getText().equals("") && amount > 0) {
            if (!(amount > balance)) {
                new Form_SubmitWarning(this, "Recovery Amount").setVisible(true);
                this.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "Amount is more than Balance!");
                fieldAmount.setText("");
                fieldAmount.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Invalid Amount!");
            fieldAmount.setText("");
            fieldAmount.requestFocus();
        }
        }catch(Exception ex){
        }
    }
    
