    @Override
    public void submitAction() {

        int saleID = parentForm_.getSelectedSaleID();
        Date recoveryDate = this.dateChooser.getDate();
        double amount = Double.valueOf(this.fieldAmount.getText());

        saleModule.submitRecoveyAmount(saleID, recoveryDate, amount);

        parentForm_.fillCreditInvoiceTable();

        this.dispose();

    }

    @Override
    public void setEnableParent() {
        parentForm_.setEnabled(true);
    }

