    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        Connection conn = DbConnMgr.getConnection();
        Statement stmt;
        PreparedStatement pstmt;
        ResultSet rs;

        try
        {
            stmt = conn.createStatement();
            rs = stmt.executeQuery("select amount from payables where mid = "+Table.getModel().getValueAt(Table.getSelectedRow(), 0).toString());
            rs.next();
            stmt.executeUpdate("Update payables set amount = "+(Double.parseDouble(this.ammField.getText())+(double) rs.getDouble(1))+" where mid = "+Table.getModel().getValueAt(Table.getSelectedRow(), 0));
             try{
            pstmt = conn.prepareStatement("create view pay_view as select p.mid as \"Mill_id\" , m.mname as \"Mill Name\" , p.amount as \"Amount\" from payables p , mill m where p.mid = m.mid order by m.mid asc");
            pstmt.executeQuery();
            pstmt = conn.prepareStatement("commit");
            pstmt.executeQuery();
            stmt = conn.createStatement();
            rs = stmt.executeQuery("select * from pay_view ");
            this.Table.setModel(DbUtils.resultSetToTableModel(rs));
            pstmt = conn.prepareStatement("drop view pay_view ");
            pstmt.executeQuery();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
    }
            this.dispose();
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_addButtonActionPerformed

